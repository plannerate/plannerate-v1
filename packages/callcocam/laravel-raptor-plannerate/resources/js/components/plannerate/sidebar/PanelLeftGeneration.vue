<script setup lang="ts">
import { router, usePage } from '@inertiajs/vue3';
import {  RefreshCw, Sparkles, X } from 'lucide-vue-next';
import { computed, onMounted, ref, watch } from 'vue';
import { toast } from 'vue-sonner';
import { reorderGondola, redistributeGondola, regenerateAuto } from '@/actions/Callcocam/LaravelRaptorPlannerate/Http/Controllers/Generation/AutoPlanogramController';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { Separator } from '@/components/ui/separator';
import { useT } from '@/composables/useT';
import type { Gondola } from '../../../types/planogram';
import CategoryConfigPanel from './CategoryConfigPanel.vue';
import ReoptimizationCadenceCard from './ReoptimizationCadenceCard.vue';

interface Props {
    open: boolean;
    gondola: Gondola;
}

interface Emits {
    (e: 'close'): void;
    (e: 'reload'): void;
}

interface AllocationEntry {
    product_id: string;
    product_name: string;
    category_name: string | null;
    abc_class: 'A' | 'B' | 'C' | null;
    is_mandatory: boolean;
    facings: number;
    facings_expanded: boolean;
    zone: 'hot' | 'cold' | 'neutral';
    role: string | null;
    has_target_stock: boolean;
}

interface RejectedEntry {
    product_id: string;
    product_name: string;
    category_name: string | null;
    abc_class: 'A' | 'B' | 'C' | null;
    motivo: string;
    motivo_label: string;
}

interface StoredReport {
    gondola_id: string;
    generated_at: string;
    posicionados: number;
    total_produtos: number;
    rejeitados_espaco: number;
    rejeitados_altura: number;
    rejeitados_sem_dimensao: number;
    taxa_cobertura: number;
    score_type: string | null;
    allocated: AllocationEntry[];
    rejected: RejectedEntry[];
}

const props = defineProps<Props>();

defineEmits<Emits>();
const { t } = useT();
const page = usePage();

const STORAGE_KEY = (id: string) => `plannerate_gen_report_${id}`;

const modeLabel: Record<string, string> = {
    template: 'Template',
    automatic: 'Automático',
    manual: 'Manual',
};

const modeVariant: Record<string, 'default' | 'secondary' | 'outline'> = {
    template: 'default',
    automatic: 'secondary',
    manual: 'outline',
};

const currentMode = props.gondola.generation_mode ?? 'manual';
const isReordering = ref(false);
const isRedistributing = ref(false);
const isPromoting = ref(false);
const isRegeneratingAuto = ref(false);

/** True quando o template vinculado à gôndola foi gerado pelo modo automático */
const isAutoGeneratedTemplate = computed(
    () => props.gondola.template_origin === 'auto',
);

/** URL de promoção do template: POST /planogram-templates/{id}/promote */
function promoteUrl(templateId: string): string {
    return `/planogram-templates/${templateId}/promote`;
}

/** Relatório persistido em localStorage — sobrevive a navegações */
const storedReport = ref<StoredReport | null>(null);

function loadStoredReport(): void {
    try {
        const raw = localStorage.getItem(STORAGE_KEY(props.gondola.id));
        storedReport.value = raw ? (JSON.parse(raw) as StoredReport) : null;
    } catch {
        storedReport.value = null;
    }
}

function saveReport(cr: any): void {
    const report: StoredReport = {
        gondola_id: props.gondola.id,
        generated_at: new Date().toISOString(),
        posicionados: cr.posicionados ?? 0,
        total_produtos: cr.total_produtos ?? 0,
        rejeitados_espaco: cr.rejeitados_espaco ?? 0,
        rejeitados_altura: cr.rejeitados_altura ?? 0,
        rejeitados_sem_dimensao: cr.rejeitados_sem_dimensao ?? 0,
        taxa_cobertura: cr.taxa_cobertura ?? 0,
        score_type: cr.score_type ?? null,
        allocated: cr.explanation_report?.allocated ?? [],
        rejected: cr.explanation_report?.rejected ?? [],
    };

    try {
        localStorage.setItem(STORAGE_KEY(props.gondola.id), JSON.stringify(report));
    } catch { /* quota exceeded — ignora */ }

    storedReport.value = report;
}

/** Quando o flash traz um novo capacity_report, persiste no localStorage */
watch(
    () => (page.props.flash as any)?.capacity_report,
    (cr) => {
 if (cr) {
saveReport(cr);
} 
},
    { immediate: true },
);

onMounted(loadStoredReport);

/** Métricas derivadas do relatório armazenado */
const abcStats = computed(() => {
    const list = storedReport.value?.allocated ?? [];
    const total = list.length;
    const a = list.filter((e) => e.abc_class === 'A').length;
    const b = list.filter((e) => e.abc_class === 'B').length;
    const c = list.filter((e) => e.abc_class === 'C').length;
    const sem = list.filter((e) => e.abc_class === null).length;

    return { total, a, b, c, sem };
});

const zoneStats = computed(() => {
    const list = storedReport.value?.allocated ?? [];
    const hot = list.filter((e) => e.zone === 'hot').length;
    const cold = list.filter((e) => e.zone === 'cold').length;
    const neutral = list.filter((e) => e.zone === 'neutral').length;

    return { hot, cold, neutral };
});

function barWidth(value: number, total: number): string {
    if (total === 0) {
return '0%';
}

    return `${Math.round((value / total) * 100)}%`;
}

function coverageColor(pct: number): string {
    if (pct >= 0.8) {
return 'text-emerald-600 dark:text-emerald-400';
}

    if (pct >= 0.5) {
return 'text-yellow-600 dark:text-yellow-400';
}

    return 'text-red-600 dark:text-red-400';
}

function formattedDate(iso: string): string {
    try {
        return new Date(iso).toLocaleString('pt-BR', { dateStyle: 'short', timeStyle: 'short' });
    } catch {
        return iso;
    }
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars -- card "Reordenar" parado, template comentado abaixo
function handleReorder() {
    isReordering.value = true;
    router.post(reorderGondola.url(props.gondola.id), {}, {
        preserveScroll: true,
        only: ['record'],
        onSuccess: () => toast.success(t('plannerate.sidebar.generation.reorder.success')),
        onError: () => toast.error(t('plannerate.sidebar.generation.reorder.error')),
        onFinish: () => {
 isReordering.value = false; 
},
    });
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars -- card "Redistribuir" parado, template comentado abaixo
function handleRedistribute() {
    isRedistributing.value = true;
    router.post(redistributeGondola.url(props.gondola.id), {}, {
        preserveScroll: true,
        only: ['record'],
        onSuccess: () => toast.success(t('plannerate.sidebar.generation.redistribute.success')),
        onError: () => toast.error(t('plannerate.sidebar.generation.redistribute.error')),
        onFinish: () => {
 isRedistributing.value = false; 
},
    });
}

function handlePromote() {
    if (!props.gondola.template_id) {
        return;
    }

    isPromoting.value = true;
    router.post(promoteUrl(props.gondola.template_id), {}, {
        preserveScroll: true,
        preserveState: false, 
        onSuccess: () => toast.success(t('plannerate.sidebar.generation.auto_generated.promote_success')),
        onError: () => toast.error(t('plannerate.sidebar.generation.auto_generated.promote_error')),
        onFinish: () => {
 isPromoting.value = false; 
},
    });
}

function handleRegenerateAuto() {
    isRegeneratingAuto.value = true;
    router.post(regenerateAuto.url(props.gondola.id), {}, {
        preserveScroll: true,
        only: ['record'],
        onSuccess: () => toast.success(t('plannerate.sidebar.generation.auto_generated.regenerate_auto_success')),
        onError: () => toast.error(t('plannerate.sidebar.generation.auto_generated.regenerate_auto_error')),
        onFinish: () => {
 isRegeneratingAuto.value = false; 
},
    });
}
</script>

<template>
    <Transition
        enter-active-class="transition-transform duration-300 ease-out"
        enter-from-class="-translate-x-full"
        enter-to-class="translate-x-0"

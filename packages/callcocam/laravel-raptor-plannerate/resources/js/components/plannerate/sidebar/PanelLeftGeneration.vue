            <!-- Template info -->
            <div v-if="gondola.template_id" class="border-b border-border bg-muted/30 px-4 py-2">
                <p class="text-xs text-muted-foreground">{{ t('plannerate.sidebar.generation.template_in_use') }}</p>
                <p class="mt-0.5 text-xs font-medium text-foreground">{{ gondola.template_name ?? gondola.template_id }}</p>
            </div>

            <!-- Content -->
            <div class="flex flex-1 flex-col gap-3 overflow-y-auto p-4">

                <!-- Card: Template gerado automaticamente -->
                <div
                    v-if="isAutoGeneratedTemplate && gondola.template_id"
                    class="rounded-lg border border-violet-200 bg-violet-50 p-4 shadow-sm dark:border-violet-800 dark:bg-violet-950"
                >
                    <div class="flex items-start gap-3">
                        <div class="mt-0.5 rounded-md bg-violet-500/10 p-2">
                            <Sparkles class="size-4 text-violet-600 dark:text-violet-400" />
                        </div>
                        <div class="flex-1">
                            <p class="text-sm font-medium text-violet-900 dark:text-violet-100">
                                {{ t('plannerate.sidebar.generation.auto_generated.title') }}
                            </p>
                            <p class="mt-1 text-xs text-violet-700 dark:text-violet-300">
                                {{ t('plannerate.sidebar.generation.auto_generated.description') }}
                            </p>
                            <div class="mt-3 flex flex-col gap-2">
                                <Button
                                    variant="default"
                                    size="sm"
                                    class="w-full bg-violet-600 hover:bg-violet-700 text-white"
                                    :disabled="isPromoting"
                                    @click="handlePromote"
                                    type="button"
                                >
                                    <span v-if="isPromoting" class="mr-2 size-3.5 animate-spin rounded-full border-2 border-current border-t-transparent" />
                                    <Sparkles v-else class="mr-2 size-3.5" />
                                    {{ t('plannerate.sidebar.generation.auto_generated.promote') }}
                                </Button>
                                <Button
                                    variant="outline"
                                    size="sm"
                                    class="w-full"
                                    :disabled="isRegeneratingAuto"
                                    @click="handleRegenerateAuto"
                                    type="button"
                                >
                                    <span v-if="isRegeneratingAuto" class="mr-2 size-3.5 animate-spin rounded-full border-2 border-current border-t-transparent" />
                                    <RefreshCw v-else class="mr-2 size-3.5" />
                                    {{ t('plannerate.sidebar.generation.auto_generated.regenerate_auto') }}
                                </Button>
                            </div>
                        </div>
                    </div>
                </div>

                <Separator v-if="isAutoGeneratedTemplate && gondola.template_id" />

                <!-- Configurações por categoria -->
                <CategoryConfigPanel :gondola="gondola" />

                <Separator />

                <!-- Reotimização contínua -->
                <ReoptimizationCadenceCard :gondola="gondola" />

                <!-- Relatório da última geração -->
                <template v-if="storedReport">
                    <Separator />

                    <div class="rounded-lg border border-border bg-muted/20 p-3">

                                {{ formattedDate(storedReport.generated_at) }}
                            </span>
                        </div>

                        <!-- Métricas principais -->
                        <div class="mb-3 grid grid-cols-2 gap-2">
                            <div class="rounded bg-emerald-50 px-2 py-1.5 dark:bg-emerald-950/30">
                                <p class="text-[10px] uppercase text-emerald-600 dark:text-emerald-400">Posicionados</p>
                                <p class="text-lg font-bold text-emerald-700 dark:text-emerald-300">
                                    {{ storedReport.posicionados }}
                                    <span class="text-xs font-normal text-emerald-500">/{{ storedReport.total_produtos }}</span>
                                </p>
                            </div>
                            <div class="rounded bg-muted px-2 py-1.5">
                                <p class="text-[10px] uppercase text-muted-foreground">Cobertura</p>
                                <p class="text-lg font-bold" :class="coverageColor(storedReport.taxa_cobertura)">
                                    {{ Math.round(storedReport.taxa_cobertura * 100) }}%
                                </p>
                            </div>
                            <div v-if="storedReport.rejeitados_espaco > 0" class="rounded bg-amber-50 px-2 py-1.5 dark:bg-amber-950/30">
                                <p class="text-[10px] uppercase text-amber-600 dark:text-amber-400">Sem espaço</p>
                                <p class="text-lg font-bold text-amber-700 dark:text-amber-300">{{ storedReport.rejeitados_espaco }}</p>
                            </div>
                            <div v-if="storedReport.rejeitados_sem_dimensao > 0" class="rounded bg-purple-50 px-2 py-1.5 dark:bg-purple-950/30">

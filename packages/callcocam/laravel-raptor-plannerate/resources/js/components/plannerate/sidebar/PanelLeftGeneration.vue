                            <div v-if="abcStats.a > 0" class="flex items-center gap-2">
                                <span class="w-4 shrink-0 text-[10px] font-bold text-emerald-600">A</span>
                                <div class="h-2 flex-1 overflow-hidden rounded-full bg-muted">
                                    <div class="h-full rounded-full bg-emerald-500 transition-all" :style="{ width: barWidth(abcStats.a, abcStats.total) }" />
                                </div>
                                <span class="w-5 shrink-0 text-right text-[10px] text-muted-foreground">{{ abcStats.a }}</span>
                            </div>
                            <div v-if="abcStats.b > 0" class="flex items-center gap-2">
                                <span class="w-4 shrink-0 text-[10px] font-bold text-yellow-600">B</span>
                                <div class="h-2 flex-1 overflow-hidden rounded-full bg-muted">
                                    <div class="h-full rounded-full bg-yellow-400 transition-all" :style="{ width: barWidth(abcStats.b, abcStats.total) }" />
                                </div>
                                <span class="w-5 shrink-0 text-right text-[10px] text-muted-foreground">{{ abcStats.b }}</span>
                            </div>
                            <div v-if="abcStats.c > 0" class="flex items-center gap-2">
                                <span class="w-4 shrink-0 text-[10px] font-bold text-orange-500">C</span>
                                <div class="h-2 flex-1 overflow-hidden rounded-full bg-muted">
                                    <div class="h-full rounded-full bg-orange-400 transition-all" :style="{ width: barWidth(abcStats.c, abcStats.total) }" />
                                </div>
                                <span class="w-5 shrink-0 text-right text-[10px] text-muted-foreground">{{ abcStats.c }}</span>
                            </div>
                            <div v-if="abcStats.sem > 0" class="flex items-center gap-2">
                                <span class="w-4 shrink-0 text-[10px] font-bold text-slate-400">—</span>
                                <div class="h-2 flex-1 overflow-hidden rounded-full bg-muted">
                                    <div class="h-full rounded-full bg-slate-300 transition-all dark:bg-slate-600" :style="{ width: barWidth(abcStats.sem, abcStats.total) }" />
                                </div>
                                <span class="w-5 shrink-0 text-right text-[10px] text-muted-foreground">{{ abcStats.sem }}</span>
                            </div>
                        </div>

                        <!-- Zonas térmicas -->
                        <div v-if="abcStats.total > 0 && (zoneStats.hot > 0 || zoneStats.cold > 0)" class="mb-3 space-y-1.5">
                            <p class="text-[10px] font-semibold uppercase tracking-wide text-muted-foreground">Zonas Térmicas</p>
                            <div class="flex gap-3 text-xs">
                                <span v-if="zoneStats.hot > 0" class="flex items-center gap-1">
                                    <span class="inline-block h-2 w-2 rounded-full bg-orange-400" />
                                    <span class="text-muted-foreground">Quente</span>
                                    <span class="font-medium">{{ zoneStats.hot }}</span>
                                </span>
                                <span v-if="zoneStats.cold > 0" class="flex items-center gap-1">
                                    <span class="inline-block h-2 w-2 rounded-full bg-blue-400" />
                                    <span class="text-muted-foreground">Fria</span>
                                    <span class="font-medium">{{ zoneStats.cold }}</span>
                                </span>
                                <span v-if="zoneStats.neutral > 0" class="flex items-center gap-1">
                                    <span class="inline-block h-2 w-2 rounded-full bg-slate-300 dark:bg-slate-600" />
                                    <span class="text-muted-foreground">Neutra</span>
                                    <span class="font-medium">{{ zoneStats.neutral }}</span>
                                </span>
                            </div>
                        </div>

                        <!-- Score type -->
                        <p v-if="storedReport.score_type && storedReport.score_type !== 'neutral'" class="text-[10px] text-muted-foreground">
                            Score: <span class="font-medium capitalize">{{ storedReport.score_type }}</span>
                        </p>
                        <p v-else-if="storedReport.score_type === 'neutral'" class="text-[10px] text-blue-500">
                            Sem dados de venda no período — ordenação neutra
                        </p>
                    </div>
                </template>

